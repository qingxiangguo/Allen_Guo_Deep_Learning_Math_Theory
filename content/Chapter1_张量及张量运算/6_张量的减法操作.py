import torch

def add_and_mul():
    x = torch.tensor([[[1, 2, 3],    # 根据数组生成一个三维张量
                       [4, 5, 6]],

                      [[7, 8, 9],
                       [10, 11, 12]]])
    y = torch.tensor([1, 2, 3])  # 一个一维张量
    y = y - x
    print(y)

add_and_mul()

